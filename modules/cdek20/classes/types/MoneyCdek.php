class MoneyCdek extends AbstractTypeCdek
{
    protected $value;
    protected $vat_sum;
    protected $vat_rate;

    public function getValue()
    {
        return $this->value;
    }

    public function setValue($val)
    {
        // Переведено в тип String иначе при использованнии json_encode при записи в базу округление слетит
        $this->value =  (string)Tools::ps_round($val, 2);
        return $this;
    }
}
